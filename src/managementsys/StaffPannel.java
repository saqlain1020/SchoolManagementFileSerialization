    public StaffPannel() {
        initComponents();
        try{
            FileInputStream fi = new FileInputStream(new File("Staff Data.txt")); //Read File Staff data.txt
            ObjectInputStream oi = new ObjectInputStream(fi); //setup object stream
            staffObjects.addAll((ArrayList)oi.readObject()); //read object from file then save in student objects
            oi.close(); //close object stream
            fi.close(); //close file
        }catch (Exception ex){ }
        createTable();
    }
    //Method to create table
    void createTable(){  //Go to event file to understand what is going on here.....
        String data[][] = new String[staffObjects.size()][5];
        String column[]={"ERN NO","NAME","FATHER NAME","PH NO","ADDRESS"};
        for(int i = 0; i<staffObjects.size();i++){
            data[i][0] = String.valueOf(((Staff)staffObjects.get(i)).getErnNo());
            data[i][1] = ((Staff)staffObjects.get(i)).getName();
            data[i][2] = ((Staff)staffObjects.get(i)).getFName();
            data[i][3] = ((Staff)staffObjects.get(i)).getAddress();
            data[i][4] = ((Staff)staffObjects.get(i)).getPhNo();
        }
        DefaultTableModel tableModel = new DefaultTableModel(3,3);
        tableModel.setDataVector(data,column);
        table.setModel(tableModel);
    }
    // Method to save Staff Array in file Staff Data.txt
    private void saveStaffObjects(){
        try{
            FileOutputStream f = new FileOutputStream(new File("Staff Data.txt")); //Create File
            ObjectOutputStream o = new ObjectOutputStream(f); //Setup Object output stream
            o.writeObject(staffObjects); //Write Object to file
            o.close(); //close object stream
            f.close(); //close file
        }catch(IOException e){}
    }
    
