    private void searchErnNoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchErnNoButtonActionPerformed
        // TODO add your handling code here:
        int ernNo = Integer.parseInt(searchErnNo.getText()); //get ern no from user input
        int i; 
        for(i=0;i<staffObjects.size();i++){ //loop check every entry in staff array
            if(((Staff)staffObjects.get(i)).checkErnNo(ernNo)){ //check if ern is same if yes break loop
                break;
            }
        }
        Staff temp = ((Staff)staffObjects.get(i)); //save required staff in temp 
        JOptionPane.showMessageDialog(jPanel1,"Ern No: "+temp.getErnNo()+"\nName: "+temp.getName()+"\nFather Name: "+temp.getFName()+"\nPhone No: "+temp.getPhNo()+"\nAddress: "+temp.getAddress()); // display infor
        searchErnNo.setText(""); //clear field
    }//GEN-LAST:event_searchErnNoButtonActionPerformed

    private void removeErnNoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeErnNoButtonActionPerformed
        // TODO add your handling code here:
        int ernNo = Integer.parseInt(removeErnNo.getText()); //get ern from user
        int i; 
        for(i=0;i<staffObjects.size();i++){ //loop to check every entry
            if(((Staff)staffObjects.get(i)).checkErnNo(ernNo)){ //break loop if ern is same
                break;
            }
        }
        staffObjects.remove(i); //remove entry stored at i index in staff array
        createTable(); //create table
        removeErnNo.setText(""); //clear field
        saveStaffObjects(); //save staff array in file
    }//GEN-LAST:event_removeErnNoButtonActionPerformed

